--- Platform Earning
  CREATE TABLE public.platform_earnings (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    source_type public.source_type not null,
    source_id UUID NOT NULL, --- Either link to a escort_boots row or escort_subscriptions row
    profile_id UUID NOT NULL references public.profiles (id) on delete cascade,
    tier public.subscription_tier,
    package_label text,
    amount numeric(12,2) not null,
    currency text not null,
    earned_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    notes TEXT,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
  );

  CREATE INDEX idx_platform_earnings_profile_id ON public.platform_earnings(profile_id);
  CREATE INDEX idx_platform_earnings_source_id ON public.platform_earnings(source_id);

---- JOIN TABLES
  ---- Consultant -> Escorts 1:N
    CREATE TABLE consultant_escorts (
      consultant_id UUID NOT NULL REFERENCES consultants(id) ON DELETE CASCADE,
      escort_id UUID NOT NULL REFERENCES escorts(id) ON DELETE CASCADE,
      assigned_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      PRIMARY KEY (consultant_id, escort_id)
    );

  ---- Client -> Escorts 1:N
    CREATE TABLE client_favorites (
      client_id UUID NOT NULL REFERENCES clients(id) ON DELETE CASCADE,
      escort_id UUID NOT NULL REFERENCES escorts(id) ON DELETE CASCADE,
      created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
      PRIMARY KEY (client_id, escort_id)
    );

---- Logs and Audit tables
  ---- User Activity Logs
    create table public.user_activity_logs (
      id uuid NOT NULL DEFAULT gen_random_uuid(),
      table_name text NOT NULL,
      record_id uuid,
      action text NOT NULL,
      old_data jsonb,
      new_data jsonb,
      changed_by uuid,
      created_at timestamp with time zone DEFAULT now(),
    ); 
    create index idx_user_activity_profile_id on public.user_activity_logs(profile_id);
    create index idx_user_activity_logs_created_at on public.user_activity_logs(created_at);

  ---- Audit Logs
    CREATE TABLE public.audit_logs (
      id uuid NOT NULL DEFAULT gen_random_uuid(),
      table_name text NOT NULL,
      record_id uuid,
      action text NOT NULL,
      old_data jsonb,
      new_data jsonb,
      changed_by uuid,
      created_at timestamp with time zone DEFAULT now(),
      CONSTRAINT audit_logs_pkey PRIMARY KEY (id),
      CONSTRAINT data_audit_performed_by_fkey FOREIGN KEY (changed_by) REFERENCES public.profiles(id)
    ); 
    create index idx_data_audit_table_name on public.audit_logs(table_name);

    -- Track all user visits to escort profiles
    create table public.escort_profile_views (
      id uuid primary key default gen_random_uuid(),
      escort_id uuid not null references public.escorts(id) on delete cascade,
      viewer_id uuid references public.clients(id),
      created_at timestamptz not null default now()
    );
    
    -- Optional: aggregate table for faster analytics
    create table public.escort_metrics_daily (
      escort_id uuid not null references public.escorts(id) on delete cascade,
      date date not null,
      views int default 0,
      favorites int default 0,
      primary key (escort_id, date)
    );
